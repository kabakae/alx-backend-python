#!/usr/bin/env python3
"""
0-add Module
============

This module contains the function `add` which takes two floats as arguments
and returns their sum as a float.
"""

def add(a: float, b: float) -> float:
    """
    Adds two floats and returns their sum.

    Parameters:
    -----------
    a : float
        The first float to add.
    b : float
        The second float to add.

    Returns:
    --------
    float
        The sum of the two floats.
    """
    return a + b
