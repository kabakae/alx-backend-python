#!/usr/bin/env python3
"""
1-concat Module
===============

This module contains the function `concat` which takes two strings as arguments
and returns their concatenated result.
"""

def concat(str1: str, str2: str) -> str:
    """
    Concatenates two strings and returns the result.

    Parameters:
    -----------
    str1 : str
        The first string to concatenate.
    str2 : str
        The second string to concatenate.

    Returns:
    --------
    str
        The concatenated result of the two strings.
    """
    return str1 + str2
