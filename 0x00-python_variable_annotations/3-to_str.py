#!/usr/bin/env python3
"""
3-to_str Module
===============

This module contains the function `to_str` which takes a float as an argument
and returns the string representation of the float.
"""


def to_str(n: float) -> str:
    """
    Returns the string representation of the float.

    Parameters:
    -----------
    n : float
        The float number to convert to a string.

    Returns:
    --------
    str
        The string representation of the float.
    """
    return str(n)
